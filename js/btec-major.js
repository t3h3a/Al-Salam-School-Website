  const params = new URLSearchParams(window.location.search);
  const key = params.get("major");
  const major = majors[key];
  const sections = document.querySelectorAll("[data-major-section]");
  const emptySection = document.querySelector("[data-major-empty]");

  if (!major) {
    sections.forEach((section) => {
      section.hidden = true;
    });
    if (emptySection) {
      emptySection.hidden = false;
    }
    return;
  }

  sections.forEach((section) => {
    section.hidden = false;
  });
  if (emptySection) {
    emptySection.hidden = true;
  }

  document.title = `${major.title} | تفاصيل تخصص BTEC`;

  setText("#majorTitle", major.title);
  setText("#majorSummary", major.summary);
  setText("#majorFocus", major.focus);
  setText("#majorWork", major.work);
  setText("#majorFit", major.fit);

  renderTags(major.tags, document.querySelector("#majorTags"));
  renderIcon(major.icon, document.querySelector("#majorIcon"));
  renderLongform(major.longform, document.querySelector("#majorLongformContent"));
  renderList(major.skills, document.querySelector("#majorSkills"));
  renderList(major.tasks, document.querySelector("#majorTasks"));
  renderList(major.assignments, document.querySelector("#majorAssignments"));

  function setText(selector, value) {
    const el = document.querySelector(selector);
    if (el) {
      el.textContent = value;
    }
  }

  function renderTags(tags, container) {
    if (!container) {
      return;
    }
    container.innerHTML = "";
    tags.forEach((tag) => {
      const span = document.createElement("span");
      span.className = "major-tag";
      span.textContent = tag;
      container.appendChild(span);
    });
  }

  function renderIcon(icon, container) {
    if (!container) {
      return;
    }
    container.innerHTML = "";
    const iconEl = document.createElement("i");
    iconEl.className = `fa-solid ${icon}`;
    container.appendChild(iconEl);
  }

  function renderList(items, container) {
    if (!container) {
      return;
    }
    container.innerHTML = "";
    items.forEach((item) => {
      const li = document.createElement("li");
      li.textContent = item;
      container.appendChild(li);
    });
  }

  function renderLongform(longform, container) {
    if (!container || !longform) {
      return;
    }
    container.innerHTML = "";
    if (longform.intro) {
      const intro = document.createElement("p");
      intro.textContent = longform.intro;
      container.appendChild(intro);
    }
    if (longform.bullets && longform.bullets.length) {
      const ul = document.createElement("ul");
      longform.bullets.forEach((item) => {
        const li = document.createElement("li");
        li.textContent = item;
        ul.appendChild(li);
      });
      container.appendChild(ul);
    }
    if (longform.closing) {
      const closing = document.createElement("p");
      closing.textContent = longform.closing;
      container.appendChild(closing);
    }
  }
